    private void btnGravarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnGravarMouseClicked
        if(comboboxLavoura.getSelectedIndex() > 0) {
            try {
                int codigoPlanta = comboboxCultivar.getSelectedIndex();
                int codigoLavoura = comboboxLavoura.getSelectedIndex() - 1;
                Planta auxPlanta = this.listaPlanta.get(codigoPlanta);
                Lavoura auxLavoura = this.listaLavoura.get(codigoLavoura);
                Entrega auxEntrega = new Entrega(auxLavoura, auxPlanta, Integer.parseInt(comboboxSafra.getSelectedItem().toString()), (Integer) spinnerQtd.getValue(), txtData.getText());
                auxEntrega.codigo = this.entregaAtual.codigo;
                if(this.entregaAtual != null) {
                    EntregaDAO.update(auxEntrega);
                }
                else {
                    EntregaDAO.insert(auxEntrega);
                }
                Object[] options = {"Sim", "NÃ£o"};
                int opcao = JOptionPane.showOptionDialog(null, "Deseja cadastrar outra entrega?", "Alerta", JOptionPane.DEFAULT_OPTION, JOptionPane.QUESTION_MESSAGE, null, options, options[0]);
                if(opcao == 0){
                    TelaEntrega te = new TelaEntrega();
                    te.setVisible(true);
                    if(this.lavouraAtual != null) {
                        te.setLavoura(this.lld, this.lavouraAtual);
                    }
                }
                if(this.lld != null) {
                    this.lld.atualizarTabelaProdutividade();
                }
                dispose();
            }
            catch(Exception ex) {
                JOptionPane.showMessageDialog(null, "Erro: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_btnGravarMouseClicked

    private void comboboxTipoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_comboboxTipoActionPerformed
        comboboxCultivar.removeAllItems();
        if(comboboxTipo.getSelectedIndex() > 0) {            
            attCultivar();
        }
        else {
            comboboxCultivar.setEnabled(false);
        }
    }//GEN-LAST:event_comboboxTipoActionPerformed

    public void setHoverEffects() {
        javax.swing.JLabel lista[] = {btnCancelar, btnGravar};
        for(javax.swing.JLabel obj : lista) {            
            obj.addMouseListener(new MouseAdapter() {
                @Override
                public void mouseEntered(MouseEvent e) {
                    super.mouseEntered(e);
                    obj.setSize(obj.getWidth() + 3, obj.getHeight() + 3);obj.setForeground(Color.WHITE);
                    if(obj.equals(btnCancelar)) {
                        obj.setBackground(new Color(255, 0, 0));
                    }
                    else {
                        obj.setBackground(new Color(153, 153, 153));                        
                    }
                }

                @Override
                public void mouseExited(MouseEvent e) {                    
                    super.mouseExited(e); 
                    obj.setSize(obj.getWidth() - 3, obj.getHeight() - 3);
                    obj.setBackground(new Color(233, 230, 230));
                    obj.setForeground(Color.BLACK);
                }
            });
        }
    }
    
    public void attComboboxes() {               
        ArrayList<String> resultClasse = PlantaDAO.getTipo();
        resultClasse.forEach((obj) -> {
            comboboxTipo.addItem(obj);
        });
        comboboxCultivar.setEnabled(false);
        ArrayList<Lavoura> resultLavoura = LavouraDAO.getLavouras();
        resultLavoura.forEach((obj) -> {
            comboboxLavoura.addItem(obj.nome);
        });
        this.listaLavoura = resultLavoura;
        for(int i = 2019; i >= 1900; i--) {
            comboboxSafra.addItem(String.valueOf(i));
        }
    }
    
    public void attCultivar() {
        ArrayList<Planta> resultPlanta = PlantaDAO.getPlantaByTipo(comboboxTipo.getSelectedItem().toString());
        resultPlanta.forEach((obj) -> {
            comboboxCultivar.addItem(obj.cultivar);
        });
        comboboxCultivar.setEnabled(true);
        this.listaPlanta = resultPlanta;
    }
    
    public void setLavoura(ListagemLavouraDetalhada lld, Lavoura lavoura) {
        comboboxLavoura.setSelectedItem(lavoura.nome);
        this.lld = lld;
        this.lavouraAtual = lavoura;
    }
    
    public void setEntrega(ListagemLavouraDetalhada lld, Entrega entrega) {
        this.lld = lld;
        this.entregaAtual = entrega;
        comboboxLavoura.setSelectedItem(entrega.lavoura.nome);
        comboboxTipo.setSelectedItem(entrega.planta.tipo);
        comboboxCultivar.setSelectedItem(entrega.planta.cultivar);
        comboboxSafra.setSelectedItem(entrega.safra);
        spinnerQtd.setValue(entrega.qtdEmSacas);
        txtData.setText(entrega.dataEntrega);
    }
    
